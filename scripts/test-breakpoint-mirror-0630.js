const assert = require('assert');
const fs = require('fs');
const os = require('os');
const path = require('path');
const { CviParser } = require('../out/model/cviParser');
const parser = new CviParser();
const root = fs.mkdtempSync(path.join(os.tmpdir(), 'cvi-mirror-0630-'));
const { workspacePath, projectPath } = parser.createWorkspaceAndProject(root, 'Mirror', 'Application', 'Executable', undefined, 2000);
const source = path.join(root, 'Main.c');
fs.writeFileSync(source, 'int main(void){return 0;}\n');
assert.strictEqual(parser.addFilesToProject(projectPath,[source]),1);
let cws=fs.readFileSync(workspacePath,'utf8');
cws=cws.replace('[Default Build Config 0001 Debug]', `[File 0001]\nPath = "${source.replace(/\\/g,'/')}"\nFile Type = "CSource"\nIn Projects = "1,"\nSource Window State = "minimal"\nBreakpoint 0001 = "39,0,enabled,"\nBreakpoint 0002 = "43,0,enabled,"\nTracepoint 0001 = "0,0,44,1,"\n\n[Default Build Config 0001 Debug]`);
fs.writeFileSync(workspacePath,cws);
const result=parser.synchronizeWorkspaceBreakpoints(workspacePath,1,projectPath,[{filePath:source,line:39},{filePath:source,line:45}],[],false);
const after=fs.readFileSync(workspacePath,'utf8');
assert(after.includes('Breakpoint 0001 = "39,0,enabled,"'));
assert(after.includes('Breakpoint 0002 = "45,0,enabled,"'));
assert(!after.includes('"43,0,enabled,"'));
assert(after.includes('Tracepoint 0001 = "0,0,44,1,"'));
assert.strictEqual(result.removedNativeCount,1);
console.log(JSON.stringify({version:'0.6.30',finalBreakpoints:[39,45],removedNative:[43],tracepointPreserved:true},null,2));
